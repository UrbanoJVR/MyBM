    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try {
            boolean flag = false;
            boolean x = this.hayArticulosEnTabla();
            boolean y = this.datosFacturaCorrectos();
            boolean z = this.datosTiendaCorrectos();
            if (x && y && z) {
                Tienda t = new Tienda(this.nombreTiendaTxt.getText().toUpperCase(), this.webTIendaTxt.getText().toLowerCase(), this.direccionTiendaTxt.getText().toUpperCase());
                Comprador c = new Comprador(this.dniCompradorTxt.getText().toUpperCase(), this.nombreCompradorTxt.getText().toUpperCase(), this.apellidosCompradorTxt.getText().toUpperCase(), this.emailCompradorTxt.getText().toLowerCase(), this.direccionCompradorTxt.getText().toUpperCase(), this.telefonoCompradorTxt.getText());
                //Creamos el GregorianCalendar con la fecha de compra
                GregorianCalendar fecha = new GregorianCalendar(Integer.parseInt(this.añoFechaTxt.getText()), (Integer.parseInt(this.mesFechaTxt.getText()) - 1), Integer.parseInt(this.diaFechaTxt.getText()));
                //Deducimos la forma de pago:
                String formaPago = null;
                if (this.formaPagoComboBox.getSelectedIndex() != 9) {
                    formaPago = this.formaPagoComboBox.getSelectedItem().toString().toUpperCase();
                } else {
                    formaPago = this.otraFormaPagoTxt.getText().toUpperCase();
                }
                //Deducimos los gastos de envío SIN IVA:
                double gEnvioSinIVA;
                if (this.gastosEnvioConIvaRadioButon.isSelected()) {
                    //Incluyen el iva, hay que quitarlo:
                    gEnvioSinIVA = Double.parseDouble(this.gastosEnvioTxt.getText()) / 1.21;
                } else {
                    gEnvioSinIVA = Double.parseDouble(this.gastosEnvioTxt.getText());
                }
                //Deducimos cuanto es el descuento CON IVA:
                double descuentoConIVA = 0;
                if (this.contieneDescuentoRadioButon.isSelected()) {
                    if (this.descuentoConIvaRadioButon.isSelected()) {
                        descuentoConIVA = Double.parseDouble(this.descuentoTxt.getText());
                    } else {
                        descuentoConIVA = Double.parseDouble(this.descuentoTxt.getText()) + (Double.parseDouble(this.descuentoTxt.getText()) * 0.21);
                    }
                }
                //Ya tenemos todo lo necesario. Ahora, creamos la factura
                Factura f = new Factura(t, c, fecha, Integer.parseInt(this.numeroPedidoTxt.getText()), formaPago, this.listaProductos, gEnvioSinIVA, descuentoConIVA);
                //La factura está creada, hay que añadir la factura al programa para que la guarde.
                Control.añadirFactura(f);
                this.padre.escribirTabla(Control.facturas);
                flag = true;
                if (flag) {
                    JOptionPane.showMessageDialog(this, "Factura añadida con éxito", "Operación finalizada", JOptionPane.PLAIN_MESSAGE);
                }
            }
        } catch (NumberFormatException | HeadlessException error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Error en inserción de datos", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

