    private void insertarArticuloButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarArticuloButonActionPerformed
        // TODO add your handling code here:
        boolean todoCorrecto = true;
        if (this.nombreArticuloTxt.getText().toUpperCase().equals("NOMBRE DEL ARTÍCULO") || this.nombreArticuloTxt.getText().isEmpty()) {
            todoCorrecto = false;
        }
        if (this.tipoArticuloTxt.getText().toUpperCase().equals("CATEGORÍA") || this.tipoArticuloTxt.getText().isEmpty()) {
            todoCorrecto = false;
        }
        if (this.codigoArticuloTxt.getText().toUpperCase().equals("IDENTIFICADOR DEL ARTÍCULO") || this.codigoArticuloTxt.getText().isEmpty()) {
            todoCorrecto = false;
        }
        if (this.precioArticuloTxt.getText().toUpperCase().equals("€") || this.precioArticuloTxt.getText().isEmpty()) {
            todoCorrecto = false;
        }
        if (todoCorrecto) {
            try {
                Producto p = new Producto(this.nombreArticuloTxt.getText().toUpperCase(), this.tipoArticuloTxt.getText().toUpperCase(), Integer.parseInt(this.codigoArticuloTxt.getText()), Double.parseDouble(this.precioArticuloTxt.getText()), this.precioArticuloIncluyeIvaRadioButon.isSelected());
                this.listaProductos.add(p);
                this.actualizarTabla();
                this.limpiarArticuloButonActionPerformed(evt);
            } catch (Exception error) {
                JOptionPane.showMessageDialog(this, error.getMessage(), "Error al guardar artículo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, rellene todos los campos correctamente", "No se puede añadir el artículo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_insertarArticuloButonActionPerformed

