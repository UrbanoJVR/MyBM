    public void actualizarTabla() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) this.tablaArticulos.getModel();
            modelo.setRowCount(0);
            for (int i = 0; i < listaProductos.size(); i++) {
                modelo = (DefaultTableModel) this.tablaArticulos.getModel();
                modelo.setRowCount(i + 1);
                this.tablaArticulos.setValueAt(this.listaProductos.get(i).getCodigo(), i, 0);
                this.tablaArticulos.setValueAt(this.listaProductos.get(i).getNombre(), i, 1);
                this.tablaArticulos.setValueAt(this.listaProductos.get(i).getPrecioConIVA(), i, 2);
            }
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Error escribiendo datos en tabla", JOptionPane.ERROR_MESSAGE);
            //Añadir evento error escribir tabla mientras se creaba factura
        }
    }

    public boolean hayArticulosEnTabla() {
        if (this.tablaArticulos.getRowCount() >= 1 && !this.listaProductos.isEmpty()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "No hay artículos para insertar en la factura", "Error al crear factura", JOptionPane.ERROR_MESSAGE);
            //Añadir evento de error al crear factura por no haber artículos
            return false;
        }
    }

    public boolean datosFacturaCorrectos() {
        boolean r = true;
        if (this.numeroPedidoTxt.getText().equals("Número de pedido")) {
            r = false;
        }
        if (this.diaFechaTxt.getText().equals("Día")) {
            r = false;
        }
        if (this.mesFechaTxt.getText().equals("Mes")) {
            r = false;
        }
        if (this.añoFechaTxt.getText().equals("Año")) {
            r = false;
        }
        if (this.formaPagoComboBox.getSelectedIndex() == 0) {
            r = false;
        }
        if (this.gastosEnvioTxt.getText().equals("€")) {
            r = false;
        }
        if (!r) {
            JOptionPane.showMessageDialog(this, "Por favor, rellene adecuadamente los datos de facturación", "Error al crear factura", JOptionPane.ERROR_MESSAGE);
        }
        return r;
    }

    public boolean datosTiendaCorrectos() {
        boolean r = true;
        if (this.nombreTiendaTxt.getText().equals("Nombre de la tienda")) {
            r = false;
        }
        if (this.webTIendaTxt.getText().equals("Página web")) {
            r = false;
        }
        if (this.direccionTiendaTxt.getText().equals("Dirección")) {
            r = false;
        }
        if (!r) {
            JOptionPane.showMessageDialog(this, "Por favor, rellene adecuadamente los datos de la tienda", "Error al crear factura", JOptionPane.ERROR_MESSAGE);
        }
        return r;
    }

